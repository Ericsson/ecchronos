import logging
import os
import subprocess
import sys
import time

import pytest

import global_variables as global_vars
from conftest import build_behave_command

logger = logging.getLogger(__name__)

OPENAPI_FETCH_RETRIES = 10
OPENAPI_FETCH_RETRY_DELAY_SECS = 3
BEHAVE_TIMEOUT_SECS = 1800
OPENAPI_TIMEOUT_SECS = 30
PYLINT_TIMEOUT_SECS = 300


def _run_command(command, timeout, capture_output=False, text=False):
    return subprocess.run(
        command,
        stdout=subprocess.PIPE if capture_output else sys.stdout,
        stderr=subprocess.PIPE if capture_output else sys.stderr,
        timeout=timeout,
        text=text,
        check=False,
    )


def _fetch_openapi_with_retries(openapi_path):
    if global_vars.LOCAL == "true":
        curl_cmd = ["curl", "http://localhost:8080/v3/api-docs.yaml", "-o", openapi_path]
    else:
        curl_cmd = [
            "curl",
            "https://localhost:8080/v3/api-docs.yaml",
            "-o",
            openapi_path,
            "--cert",
            f"{global_vars.CERTIFICATE_DIRECTORY}/clientcert.crt",
            "--key",
            f"{global_vars.CERTIFICATE_DIRECTORY}/clientkey.pem",
            "--cacert",
            f"{global_vars.CERTIFICATE_DIRECTORY}/serverca.crt",
        ]

    last_error = None

    for attempt in range(1, OPENAPI_FETCH_RETRIES + 1):
        logger.info(
            "Fetching OpenAPI spec (attempt %s/%s)",
            attempt,
            OPENAPI_FETCH_RETRIES,
        )

        try:
            result = _run_command(
                curl_cmd,
                timeout=OPENAPI_TIMEOUT_SECS,
                capture_output=True,
                text=True,
            )

            if result.returncode == 0 and os.path.exists(openapi_path) and os.path.getsize(openapi_path) > 0:
                return

            last_error = (
                f"curl exit code={result.returncode}, "
                f"stdout={result.stdout.strip()}, stderr={result.stderr.strip()}"
            )
        except subprocess.TimeoutExpired as exc:
            last_error = f"curl timed out: {exc}"

        if attempt < OPENAPI_FETCH_RETRIES:
            time.sleep(OPENAPI_FETCH_RETRY_DELAY_SECS)

    pytest.fail(f"Failed to fetch OpenAPI spec after retries: {last_error}")


@pytest.mark.dependency(name="test_behave_tests")
def test_behave_tests(test_environment):
    """Test that runs behave tests."""
    cassandra_cluster = test_environment
    command = build_behave_command(cassandra_cluster)
    logger.info("Running behave tests")

    try:
        result = _run_command(command, timeout=BEHAVE_TIMEOUT_SECS)
        logger.info("Behave tests completed with exit code: %s", result.returncode)

        if result.returncode != 0:
            logger.error("Behave tests failed")
            pytest.fail("Behave tests failed")
        else:
            logger.info("Behave tests passed successfully")

    except subprocess.TimeoutExpired:
        logger.error("Behave tests timed out after 30 minutes")
        pytest.fail("Behave tests timed out")
    except subprocess.SubprocessError as exc:
        logger.error("Failed to run behave tests: %s", exc)
        pytest.fail(f"Failed to run behave tests: {exc}")


@pytest.mark.dependency(name="test_fetch_and_validate_openapi", depends=["test_behave_tests"])
def test_fetch_and_validate_openapi(test_environment):
    """Test that fetches and validates OpenAPI spec."""
    openapi_path = "../../../docs/autogenerated/openapi.yaml"

    try:
        _fetch_openapi_with_retries(openapi_path)

        validate_cmd = ["openapi-spec-validator", openapi_path, "--schema", "3.0.0"]
        result = _run_command(validate_cmd, timeout=OPENAPI_TIMEOUT_SECS)

        if result.returncode == 0:
            logger.info("OpenAPI spec validation passed")
        else:
            logger.error("OpenAPI spec validation failed")
            pytest.fail("OpenAPI spec validation failed")

    except (subprocess.TimeoutExpired, subprocess.SubprocessError) as exc:
        logger.error("Failed to fetch/validate OpenAPI spec: %s", exc)
        pytest.fail(f"Failed to fetch/validate OpenAPI spec: {exc}")


@pytest.mark.dependency(name="test_pylint_tests", depends=["test_fetch_and_validate_openapi"])
def test_pylint_tests():
    """Test that runs pylint on specified directories."""
    directories = [
        f"{global_vars.PROJECT_BUILD_DIRECTORY}/../src/bin",
        f"{global_vars.PROJECT_BUILD_DIRECTORY}/../src/pylib/ecchronoslib",
        f"{global_vars.PROJECT_BUILD_DIRECTORY}/../src/test/behave/ecc_step_library",
        f"{global_vars.PROJECT_BUILD_DIRECTORY}/../src/test/behave/features/steps",
    ]

    for directory in directories:
        if not os.path.exists(directory):
            logger.warning("Directory %s does not exist, skipping pylint", directory)
            continue

        logger.info("Running pylint for %s", directory)
        try:
            result = _run_command(["pylint", directory], timeout=PYLINT_TIMEOUT_SECS)
            if result.returncode != 0:
                logger.error("Pylint failed for %s", directory)
                pytest.fail(f"Pylint failed for {directory}")
        except (subprocess.TimeoutExpired, subprocess.SubprocessError) as exc:
            logger.error("Failed to run pylint for %s: %s", directory, exc)
            pytest.fail(f"Failed to run pylint for {directory}: {exc}")

    logger.info("All pylint tests passed")


if __name__ == "__main__":
    pytest.main([__file__, "-v"])
